def diamond():
    glPushMatrix()
    glTranslated(500, 250, 0)

    glBegin(GL_POLYGON)
    glColor3ub(40, 255, 210)
    glVertex2f(0, -20)
    glVertex2f(-20, 5)
    glVertex2f(-10, 20)
    glVertex2f(10, 20)
    glVertex2f(20, 5)
    glVertex2f(20, 5)
    glEnd()

    glLineWidth(3)
    glBegin(GL_LINE_LOOP)
    glColor3ub(40, 195, 210)
    glVertex2f(0, -20)
    glVertex2f(-20, 5)
    glVertex2f(-10, 20)
    glVertex2f(10, 20)
    glVertex2f(20, 5)
    glVertex2f(20, 5)
    glEnd()

    glLineWidth(3)
    glBegin(GL_LINE_LOOP)
    glColor3ub(40, 195, 210)
    glVertex2f(-20, 5)
    glVertex2f(-10, 20)
    glVertex2f(-5, 13)
    glVertex2f(5, 13)
    glVertex2f(10, 20)
    glVertex2f(20, 5)
    glVertex2f(10, 3)
    glVertex2f(5, 13)
    glVertex2f(-5, 13)
    glVertex2f(-10, 3)
    glVertex2f(10, 3)
    glVertex2f(0, -20)
    glVertex2f(-10, 3)
    glEnd()

    glPopMatrix()

def showScreen():
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT)
    glClearColor(0,0,0,1)
    glLoadIdentity()
    glViewport(0, 0, 1000, 500)
    glMatrixMode(GL_PROJECTION)
    glLoadIdentity()
    glOrtho(0, 1000, 0, 500, 0.0, 1.0)
    glMatrixMode (GL_MODELVIEW)
    glLoadIdentity()
    # tes()
    # mainchar()
    diamond()
    glutSwapBuffers()

glutInit()
glutInitDisplayMode(GLUT_RGBA)
glutInitWindowSize(1000, 500)
glutInitWindowPosition(0, 0)
glutCreateWindow("OpenGL Coding Practice")
glutDisplayFunc(showScreen)
glutIdleFunc(showScreen)
glutMainLoop()